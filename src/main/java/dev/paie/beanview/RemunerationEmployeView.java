package dev.paie.beanview;

public class RemunerationEmployeView {
	
	private Integer id;
	private String matricule;
	private Integer entrepriseId;
	private Integer profilId;
	private Integer gradeId;
	
	/** Getter for id
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	/** Setter
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	/** Getter for matricule
	 * @return the matricule
	 */
	public String getMatricule() {
		return matricule;
	}
	/** Setter
	 * @param matricule the matricule to set
	 */
	public void setMatricule(String matricule) {
		this.matricule = matricule;
	}
	/** Getter for entrepriseId
	 * @return the entrepriseId
	 */
	public Integer getEntrepriseId() {
		return entrepriseId;
	}
	/** Setter
	 * @param entrepriseId the entrepriseId to set
	 */
	public void setEntrepriseId(Integer entrepriseId) {
		this.entrepriseId = entrepriseId;
	}
	/** Getter for profilId
	 * @return the profilId
	 */
	public Integer getProfilId() {
		return profilId;
	}
	/** Setter
	 * @param profilId the profilId to set
	 */
	public void setProfilId(Integer profilId) {
		this.profilId = profilId;
	}
	/** Getter for gradeId
	 * @return the gradeId
	 */
	public Integer getGradeId() {
		return gradeId;
	}
	/** Setter
	 * @param gradeId the gradeId to set
	 */
	public void setGradeId(Integer gradeId) {
		this.gradeId = gradeId;
	}
	
}
